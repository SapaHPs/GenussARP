        <section class="section buying-guide">
            <h2>Guía de Compra</h2>
            <p>Al elegir plátanos machos, busca aquellos que estén firmes y con cáscara intacta. Si prefieres utilizarlos verdes, elige los que tengan un color verde intenso. Para usos en recetas dulces o como postre, busca plátanos con cáscara amarilla o negra, indicando que están maduros.</p>
        </section>

