                <h2>Un poco Denuestra Historia</h2>
                <p>En 1969, el decreto 1962 instauró el programa INEM, dando inicio a la política de educación diversificada, complementada por el decreto 1419 de 1978 que estableció los ejes curriculares de ciencia, arte y tecnología. En 1979, se crearon los Centros Auxiliares de Servicios Docentes (CASD) y en 1984, el CASD Aldemar Rojas Plazas en Bogotá.
                La institución, con más de 20 años en educación técnica, se transformó en 2003 en el Centro Educativo Distrital Aldemar Rojas Plazas. En 2005, comenzó a articularse con la Universidad Distrital y, desde 2009, con el SENA.
                    Hoy, ofrece siete programas técnicos y es pionera en la jornada única desde 2014. En 2017, se añadió una cocina móvil y comedor escolar. En 2019, se renombró como Colegio Técnico Aldemar Rojas Plazas C.E.D., consolidando su modelo educativo y adaptando su currículo.</p>
