                <h2>Nuestra institución</h2>
                <p>Nuestra institución se destaca a nivel nacional por su innovadora renovación curricular, enfocada en la media especializada, en colaboración con el SENA. Sus principales características son:
                Educación media técnica en Jornada Única, con siete especialidades, pionera en Colombia.
                Oportunidad de formación tecnológica para egresados propios y de otras instituciones.
                Modelo pedagógico propio basado en aprendizaje dialógico y solución de problemas.
                Formación integral en autoestima, autonomía y proyecto de vida.
                Evaluación innovadora con estrategias de mejoramiento y acompañamiento personalizado.
                Ambientes pedagógicos con amplias instalaciones y dotación adecuada.
                Apoyo nutricional con comida caliente (desayuno y almuerzo) de alta calidad.
                Fomento del desarrollo profesional del personal, con un equipo comprometido y responsable.</p>
